# Using String Formatting Operator %
character = 'p'
signed_dec_int = -18.9
octal_int = 0o123
hex_dec_int = 0X7AE
float_real_num = 13.567
exp_num = 3e4

print("The character is %c" %character)
print("The Signed Decimal Integer is %d" %signed_dec_int)
print("The Octal integer is %o" %octal_int)
print("The Hexadecimal integer (Upper case) is %X" %hex_dec_int)
print("The Floating point real number is %.3f" %float_real_num)
print("The exponential number is %g" %exp_num)
