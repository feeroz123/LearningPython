# To count number of letter in a sentence

sentence ='Discover, Learning, with, Edureka'

# Number of lowercase “a” and “o” in the given sentence
print("Number of 'a' : ", sentence.count('a'))
print("Number of 'o' : ", sentence.count('o'))

# Number of uppercase “L” and “N” in the given sentence
print("Number of 'L' : ", sentence.count('L'))
print("Number of 'N' : ", sentence.count('N'))

